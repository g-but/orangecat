import { MetadataRoute } from 'next';

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: '*',
        allow: '/',
        disallow: [
          '/api/',
          '/dashboard/',
          '/settings/',
          '/messages/',
          '/onboarding/',
          '/profile/setup/',
          '/admin/',
        ],
      },
    ],
    sitemap: 'https://orangecat.ch/sitemap.xml',
  };
}
